l_warn = list(set(l_warn))


# 2.7-3.2
# https://stackoverflow.com/questions/16968188/how-do-i-avoid-the-capital-placeholders-in-pythons-argparse-module
class SingleMetavarHelpFormatter(argparse.HelpFormatter):

    def _format_action_invocation(self, action):
        if not action.option_strings:
            metavar, = self._metavar_formatter(action, action.dest)(1)
            return metavar
        else:
            parts = []
            if action.nargs == 0:
                parts.extend(action.option_strings)
            else:
                default = action.dest.upper()
                args_string = self._format_args(action, default)
                parts.extend(action.option_strings)
                parts[-1] += ' %s' % args_string
            return ', '.join(parts)


# Generate a rather simple parser
parser = argparse.ArgumentParser(
    formatter_class=SingleMetavarHelpFormatter, description="Noizu is a "
    "GCC/Clang warnings formatter which lets you exclude specific paths or "
    "filenames to suppress their warnings from stderr compiler output.", 
    add_help=False)
parser.add_argument('input', type=argparse.FileType('r'),
                    help='GCC/Clang stderr log (generated with '
                    '-fmessage-length=0)')
parser.add_argument('-e', '--exclude', metavar='', nargs='+',
                    help='Exclude warnings from these paths or filenames')
parser.add_argument('-o', '--output', metavar='',
                    type=argparse.FileType('w'), help='Output [default: stdout]')
parser.add_argument('-h', '--help', action='help', default=argparse.SUPPRESS,
                    help='Show this help message and exit')

# Get args
args = parser.parse_args()

# Search log for warnings
d_warn = {}
with args.input as f:
    for line in f:
        # Line must containg ': warning:' and a valid path
        if ': warning:' in line and re.match('(\w*\/)*\S*\.(h|hh|H|hxx|hpp|h\+\+|c|cc|C|cxx|c\+\+|cpp):', line):
            p = line.split(': warning:', 1)[0].strip('.')
            for w in l_warn:
                # Append found warning to d
                if w in line:
                    if w in d_warn:
                        # Only append if warning not already in dict
                        if p not in d_warn[w]:
                            d_warn[w].append(p)
                    else:
                        d_warn[w] = [p]

# Filter output dictionary if there is an exclude list
if args.exclude:
    d_out = {}
    for key, value in d_warn.items():
        d_out[key] = [s for s in value if not any(
            e in s.split(':', 1)[0] for e in args.exclude)]
        # There might be a chance that the list we just got was empty
        if len(d_out[key]) == 0:
            del d_out[key]
else:
    d_out = d_warn

# Print either to stdout or file
if args.output:
    f = args.output
else:
    f = sys.stdout

# First iteration to print number of warnings
max_len_w = len(max(l_warn, key=len)) + 10
for key, value in sorted(d_out.items(), key=lambda v: len(v[1]), reverse=True):
    s = '{warning:<{width}} {n}\n'.format(
        width=max_len_w, warning=key, n=len(value))
    f.write(s)

# Second iteration to print locations of warnings
for key, value in sorted(d_out.items(), key=lambda v: len(v[1]), reverse=True):
    f.write('\n')
    f.write(key + '\n')
    for w in value:
        f.write(w + '\n')
